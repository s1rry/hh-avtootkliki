"""
Шаблонные сопроводительные письма без трат AI-токенов.

Синтаксис шаблона (как в hh-applicant-tool):
  {вариант1|вариант2|вариант3}  → случайно выбирается один вариант
  %(vacancy_name)s              → подставляется название вакансии
  %(vacancy_suffix)s            → " «Название»" либо пусто, если названия нет

Зачем: раньше письмо было одинаковым байт-в-байт для всех откликов.
Теперь каждое письмо немного разное и упоминает вакансию. Токены не тратятся,
AI остаётся только для вакансий с обязательным тестом/вопросами.
"""
from __future__ import annotations

import random
import re

from app.config import settings

_CHOICE_RE = re.compile(r"\{([^{}]+)\}")

# Обезличенный запасной шаблон: используется, только если ИИ недоступен.
# Работает для ЛЮБОЙ профессии (у бота много разных пользователей), поэтому
# без привязки к конкретным навыкам. Задача — звучать как живой человек и не
# палиться шаблонными клише вроде "мои компетенции отвечают вашим требованиям".
DEFAULT_TEMPLATE = (
    "{Здравствуйте|Добрый день}! "
    "{Внимательно посмотрел вашу вакансию|Прочитал описание вакансии|"
    "Откликаюсь на вашу вакансию}%(vacancy_suffix)s "
    "{и вижу хорошее совпадение с тем, чем я занимаюсь|"
    "и задачи мне действительно близки|"
    "и роль совпадает с моим опытом и интересами}. "
    "{Хочу быть полезным команде и готов быстро включиться в работу|"
    "Готов подключиться к задачам и приносить результат с первых недель|"
    "Мне интересно то, что вы делаете, и я хочу быть частью этого}. "
    "{Буду рад созвониться и разобрать детали|"
    "Готов ответить на ваши вопросы на собеседовании|"
    "Давайте обсудим, чем я могу быть полезен}."
    "%(contacts)s"
)


def _expand_choices(text: str) -> str:
    """Заменить каждый {a|b|c} на случайный вариант (один проход)."""
    return _CHOICE_RE.sub(lambda m: random.choice(m.group(1).split("|")), text)


def render_letter(vacancy_name: str = "", template: str | None = None, contact: str | None = None) -> str:
    """Собрать готовое письмо из шаблона.

    template — если задан (напр. своё письмо пользователя), используется вместо
    стандартного. Поддерживает те же плейсхолдеры и {вариант|вариант}.
    contact — контакт для связи; None → глобальный settings.contacts.
    """
    text = _expand_choices(template or DEFAULT_TEMPLATE)
    name = (vacancy_name or "").strip()
    suffix = f" «{name}»" if name else ""
    text = text.replace("%(vacancy_suffix)s", suffix)
    text = text.replace("%(vacancy_name)s", name)
    contacts = (contact if contact is not None else settings.contacts or "").strip()
    text = text.replace("%(contacts)s", f"\n\nКонтакты: {contacts}" if contacts else "")
    return text.rstrip()
